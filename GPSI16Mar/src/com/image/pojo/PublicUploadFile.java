package com.image.pojo;

public class PublicUploadFile 
{
	private long public_data_id;
	private String view_user_type;
	private String deptName;
	private String upload_file;
	private String semesterWise;
	private String username;
	private long uploader_id;
	private String uploaded_by;
	
	public long getPublic_data_id() {
		return public_data_id;
	}
	public void setPublic_data_id(long public_data_id) {
		this.public_data_id = public_data_id;
	}
	public String getView_user_type() {
		return view_user_type;
	}
	public void setView_user_type(String view_user_type) {
		this.view_user_type = view_user_type;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getUpload_file() {
		return upload_file;
	}
	public void setUpload_file(String upload_file) {
		this.upload_file = upload_file;
	}
	public String getSemesterWise() {
		return semesterWise;
	}
	public void setSemesterWise(String semesterWise) {
		this.semesterWise = semesterWise;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public long getUploader_id() {
		return uploader_id;
	}
	public void setUploader_id(long uploader_id) {
		this.uploader_id = uploader_id;
	}
	public String getUploaded_by() {
		return uploaded_by;
	}
	public void setUploaded_by(String uploaded_by) {
		this.uploaded_by = uploaded_by;
	}
	
	
	
}
